def load_json_data(uploaded_file):
    """Загружает JSON из Экспорта ФГИС"""
    try:
        uploaded_file.seek(0)
        data = json.load(uploaded_file)
        return data
    except Exception as e:
        st.error(f"❌ Ошибка чтения JSON: {e}")
        return None

def generate_explanatory_note_text(data, legal_basis, tariff_type, tariff_value, cost_justifications, additional_info):
    """Генерирует текст пояснительной записки"""
    
    # Извлекаем данные из JSON
    header = data.get("header", {})
    application = data.get("application", {})
    revenue = application.get("revenue", {})
    articles = revenue.get("articles", [])
    period = application.get("tariff_period", {})
    sphere = application.get("activity_sphere", "")
    
    # Словарь сфер
    sphere_names = {
        "water_supply": "водоснабжение",
        "water_drainage": "водоотведение",
        "waste_management": "обращение с ТКО",
        "heat_supply": "теплоснабжение"
    }
    
    sphere_name = sphere_names.get(sphere, "коммунальные услуги")
    
    # ─────────────────────────────────────────────────────────────────────
    # Титульная часть
    # ─────────────────────────────────────────────────────────────────────
    text = f"""
ПОЯСНИТЕЛЬНАЯ ЗАПИСКА
к тарифной заявке на {period.get('start', '2025')[:4]} год

{header.get('organization_name', 'Организация')}
ИНН: {header.get('inn', '')} | КПП: {header.get('kpp', '')}
"""
    
    # ─────────────────────────────────────────────────────────────────────
    # 1. Основание подачи заявки
    # ─────────────────────────────────────────────────────────────────────
    text += f"""

═══════════════════════════════════════════════════════════════════════════
1. ОСНОВАНИЕ ПОДАЧИ ТАРИФНОЙ ЗАЯВКИ
═══════════════════════════════════════════════════════════════════════════

{legal_basis if legal_basis else 'Укажите основание подачи тарифной заявки'}

Нормативное обоснование:
• Федеральный закон от 27.07.2010 № 190-ФЗ «О теплоснабжении»
• Федеральный закон от 07.12.2011 № 416-ФЗ «О водоснабжении и водоотведении»
• Федеральный закон от 24.06.1998 № 89-ФЗ «Об отходах производства и потребления»
• Приказ ФАС России от 30.12.2020 № 1746-э «Об утверждении Методических указаний по расчету регулируемых тарифов»
"""
    
    # ─────────────────────────────────────────────────────────────────────
    # 2. Вид тарифа
    # ─────────────────────────────────────────────────────────────────────
    text += f"""

═══════════════════════════════════════════════════════════════════════════
2. ВИД УСТАНАВЛИВАЕМОГО ТАРИФА
═══════════════════════════════════════════════════════════════════════════

Вид тарифа: {tariff_type if tariff_type else 'Укажите вид тарифа'}
Сфера деятельности: {sphere_name}
Тарифный период: с {period.get('start', '01.01.2025')} по {period.get('end', '31.12.2025')}
"""
    
    # ─────────────────────────────────────────────────────────────────────
    # 3. Размер тарифа
    # ─────────────────────────────────────────────────────────────────────
    text += f"""

═══════════════════════════════════════════════════════════════════════════
3. РАЗМЕР ТАРИФА
═══════════════════════════════════════════════════════════════════════════

Устанавливаемый тариф: {tariff_value if tariff_value else 'Укажите размер тарифа'}

Обоснование размера тарифа:
Тариф рассчитан методом экономически обоснованных расходов (доходов), 
с учётом необходимых условий для качественного предоставления услуги.
"""
    
    # ─────────────────────────────────────────────────────────────────────
    # 4. Обоснование статей затрат (с НПА и письмами ФАС)
    # ─────────────────────────────────────────────────────────────────────
    text += f"""

═══════════════════════════════════════════════════════════════════════════
4. ОБОСНОВАНИЕ СТАТЕЙ ЗАТРАТ
═══════════════════════════════════════════════════════════════════════════

"""
    
    total_revenue = revenue.get("total", 0)
    
    for i, article in enumerate(articles, 1):
        code = article.get("code", "")
        name = article.get("name", "")
        amount = article.get("amount", 0)
